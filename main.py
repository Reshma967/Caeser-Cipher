t=True
while t:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    if(shift>26):
        shift=shift%26
    def caeser():
        word=""
        for i in range(0,len(text)):
            char=text[i]
            if char in alphabet:
                index_letter=alphabet.index(char)
                if (direction=="encode"):
                    new_index=index_letter+shift
                    new=alphabet[new_index]
                    word=word+new
                else:
                    new_index=index_letter-shift
                    new=alphabet[new_index]
                    word=word+new
            else:
                word=word+char
        print(f"The {direction}d result is",word)
        restart=input("Do you want to restart?")
        if(restart=="no"):
            t=False
            print("bye")
